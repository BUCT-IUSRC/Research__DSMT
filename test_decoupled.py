def save_one_txt(predn, save_conf, shape, file):
    # Save one txt result
    gn = torch.tensor(shape)[[1, 0, 1, 0]]  # normalization gain whwh
    for *xyxy, conf, cls in predn.tolist():
        xywh = (xyxy2xywh(torch.tensor(xyxy).view(1, 4)) / gn).view(-1).tolist()  # normalized xywh
        line = (cls, *xywh, conf) if save_conf else (cls, *xywh)  # label format
        with open(file, 'a') as f:
            f.write(('%g ' * len(line)).rstrip() % line + '\n')


def save_one_json(predn, jdict, path, class_map):
    # Save one JSON result {"image_id": 42, "category_id": 18, "bbox": [258.15, 41.29, 348.26, 243.78], "score": 0.236}
    image_id = int(path.stem) if path.stem.isnumeric() else path.stem
    box = xyxy2xywh(predn[:, :4])  # xywh
    box[:, :2] -= box[:, 2:] / 2  # xy center to top-left corner
    for p, b in zip(predn.tolist(), box.tolist()):
        jdict.append({
            'image_id': image_id,
            'category_id': class_map[int(p[5])],
            'bbox': [round(x, 3) for x in b],
            'score': round(p[4], 5)})

def process_batch(detections, labels, iouv):
    """
    Return correct prediction matrix
    Arguments:
        detections (array[N, 6]), x1, y1, x2, y2, conf, class
        labels (array[M, 5]), class, x1, y1, x2, y2
    Returns:
        correct (array[N, 10]), for 10 IoU levels
    """
    correct = np.zeros((detections.shape[0], iouv.shape[0])).astype(bool)
    iou = box_iou(labels[:, 1:], detections[:, :4])
    correct_class = labels[:, 0:1] == detections[:, 5]
    for i in range(len(iouv)):
        x = torch.where((iou >= iouv[i]) & correct_class)  # IoU > threshold and classes match
        if x[0].shape[0]:
            matches = torch.cat((torch.stack(x, 1), iou[x[0], x[1]][:, None]), 1).cpu().numpy()  # [label, detect, iou]
            if x[0].shape[0] > 1:
                matches = matches[matches[:, 2].argsort()[::-1]]
                matches = matches[np.unique(matches[:, 1], return_index=True)[1]]
                # matches = matches[matches[:, 2].argsort()[::-1]]
                matches = matches[np.unique(matches[:, 0], return_index=True)[1]]
            correct[matches[:, 1].astype(int), i] = True
    return torch.tensor(correct, dtype=torch.bool, device=iouv.device)

def seg_validation(model, n_segcls, valloader, device, half_precision=True):
    # Fast test during the training
    def eval_batch(model, image, image2, target, half):
        outputs = model(image, image2)
        # outputs = gather(outputs, 0, dim=0)
        pred = outputs[1]  # 1是分割
        target = target.to(device, non_blocking=True)
        pred = F.interpolate(pred, (target.shape[1], target.shape[2]), mode='bilinear', align_corners=True)
        correct, labeled = batch_pix_accuracy(pred.data, target)
        inter, union = batch_intersection_union(pred.data, target, n_segcls)
        return correct, labeled, inter, union

    half = device.type != 'cpu' and half_precision  # half precision only supported on CUDA
    if half:
        model.half()
    model.eval()
    total_inter, total_union, total_correct, total_label = 0, 0, 0, 0
    tbar = tqdm(valloader, desc='\r')
    for i, (image, image2, target, _, _) in enumerate(tbar):
        image = image.to(device, non_blocking=True)
        image = image.half() if half else image.float()
        image2 = image2.to(device, non_blocking=True)
        image2 = image2.half() if half else image2.float()
        with torch.no_grad():
            correct, labeled, inter, union = eval_batch(model, image, image2, target, half)

        total_correct += correct
        total_label += labeled
        total_inter += inter
        total_union += union
        pixAcc = 1.0 * total_correct / (np.spacing(1) + total_label)
        IoU = 1.0 * total_inter / (np.spacing(1) + total_union)
        iou_str = ','.join('%.3f' % iou for iou in IoU)
        mIoU = IoU.mean()
        tbar.set_description(
            'pixAcc: %.3f, mIoU: %.3f' % (pixAcc, mIoU))
    # print(mIoU)
    print(iou_str)
    return mIoU


